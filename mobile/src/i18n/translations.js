function getValueByPath(source, path) {
  if (!source || typeof source !== 'object') {
    return undefined;
  }

  return String(path)
    .split('.')
    .reduce((currentValue, segment) => {
      if (currentValue && typeof currentValue === 'object' && segment in currentValue) {
        return currentValue[segment];
      }

      return undefined;
    }, source);
}

function interpolate(template, values) {
  if (typeof template !== 'string') {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_, key) => {
    const replacement = values?.[key];
    return replacement === undefined || replacement === null ? `{${key}}` : String(replacement);
  });
}

export function createTranslator(language = 'en') {
  return function t(path, values = {}, fallback) {
    const localizedValue = getValueByPath(translations[language], path);

    if (typeof localizedValue === 'string') {
      return interpolate(localizedValue, values);
    }

    const englishValue = getValueByPath(translations.en, path);

    if (typeof englishValue === 'string') {
      return interpolate(englishValue, values);
    }

    if (typeof fallback === 'string') {
      return interpolate(fallback, values);
    }

    return path;
  };
}
